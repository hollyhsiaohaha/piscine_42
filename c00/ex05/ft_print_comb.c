#include <unistd.h>

void	number_loop(char i, char j, char k)
{
	while (i <= '7')
	{
		while (j <= '8')
		{	
			while (k <= '9')
			{
				write(1, &i, 1);
				write(1, &j, 1);
				write(1, &k, 1);
				if (i == '7' && j == '8' && k == '9')
				{
					break ;
				}
				write(1, ", ", 2);
				k++;
			}
			j++;
			k = j + 1;
		}
		i ++;
		j = i + 1;
		k = j + 1;
	}
}

void	ft_print_comb(void)
{
	number_loop('0', '1', '2');
}
